    private String tenKH, ngaySinh, gioiTinh, sdt, doiTuong, diaChi;

    public int getMaKH() {
        return maKH;
    }

    public int getIdGDN() {
        return idGDN;
    }

    public String getTenKH() {
        return tenKH;
    }

    public String getNgaySinh() {
        return ngaySinh;
    }

